public class Spartansy {
    private String name;
    private String massa;

    public Spartansy(String name, String massa) {
        this.name = name;
        this.massa = massa;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAge() {
        return massa;
    }

    public void setAge(String age) {
        this.massa = age;
    }

}
